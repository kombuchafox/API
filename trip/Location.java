package trip;
/** the Location class.
* @author Ian Fox*/
public  class Location implements graph.Weightable {
    /** the weight of this location.*/
    private double _weight;
    /** the y location.*/
    private double _y;
    /** the x location.*/
    private double _x;
    /** the location of this place.*/
    private String _location;
    /**creates a new location LOCATION.*/
    public void setLocation(String location) {
        _location = location;
    }
    /** sets the X coordinates of the location.*/
    public void setX(Double x) {
        _x = x;
    }
    /** set the Y coordinates of the location.*/
    public void setY(Double y) {
        _y = y;
    }
    @Override
    public void setWeight(double w) {
        _weight = w;
    }
    @Override
    public double weight() {
        return _weight;
    }
    /** @return string .*/
    public String getName() {
        return _location;
    }

}
